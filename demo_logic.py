def show_modular_examples():
    print(f"\n\n🧮 ПРИМЕРЫ МОДУЛЬНОЙ АРИФМЕТИКИ (mod 79):")
    print("=" * 50)
    
    # Пример умножения
    z2 = 25
    z3_calc = (z2 * 16) % 79
    print(f"Ключ 3: z = {z2} * 16 = {z2 * 16} mod 79 = {z3_calc}")
    
    # Пример вычитания
    s1, s3 = 5, 0
    z1, z3 = 5, 5
    s4_calc = (s1 - s3) % 79
    z4_calc = (z1 - z3) % 79
    print(f"Ключ 4: s = {s1} - {s3} = {s4_calc} mod 79")
    print(f"Ключ 4: z = {z1} - {z3} = {z4_calc} mod 79")
    
    # Пример отрицания
    s4, z4 = 5, 0
    s5_calc = (-s4) % 79
    z5_calc = (-z4) % 79
    print(f"Ключ 5: s = -{s4} = {s5_calc} mod 79")
    print(f"Ключ 5: z = -{z4} = {z5_calc} mod 79")

def main():
    demo_key_logic()
    show_modular_examples()
    
    print(f"\n\n🚀 ЗАПУСК РЕАЛЬНОГО СКРИПТА:")
    print("   python3 ecdsa_key_finder.py")
    print("\n💡 Введите k=14 для тестирования")

if __name__ == "__main__":
    main()